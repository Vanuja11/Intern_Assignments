fun calculateSquare(num: Int): Int {
    var squaredNum: Int = num * num
    return squaredNum
}

fun main() {
    println(calculateSquare(2))
    println(calculateSquare(3))
    println(calculateSquare(4))
    println(calculateSquare(5))
}
